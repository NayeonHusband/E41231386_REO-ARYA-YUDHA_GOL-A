    private static DefaultTableModel settable(ResultSet rs) throws SQLException {

        ResultSetMetaData rsmd = (ResultSetMetaData) rs.getMetaData();

        //names of columns
        Vector<String> columnnames = new Vector<String>();
        int columncount = rsmd.getColumnCount();
        for (int column = 1; column <= columncount; column++) {
            columnnames.add(rsmd.getColumnName(column));
        }

        Vector<Vector<Object>> data = new Vector<Vector<Object>>();
        while (rs.next()) {
            Vector<Object> vector = new Vector<Object>();
            for (int ci = 1; ci <= columncount; ci++) {
                vector.add(rs.getObject(ci));
            }
            data.add(vector);
        }
        return new DefaultTableModel(data, columnnames);
    }

    private void updatetabel() {
        try {
            Tabel1.setModel(settable(dbs.getConnection().createStatement().executeQuery("select kd_barang as `Kode Barang`,nama_barang as `Nama Barang`,stok as `Stock`,harga as `Harga Beli`, harga_jual as `Harga Jual`,catatan as `Catatan` from data_barang")));
        } catch (SQLException e) {
            e.printStackTrace();
        }

    }
    private String getRandomNumberString(){
        Random rand = new Random();
        return String.valueOf(rand.nextInt(999999999));
    }
    
    private void generate(String kode) throws Exception{
       Linear barcode = new Linear();
       barcode.setType(Linear.CODE128);
       
       barcode.setData(kode);
       barcode.setI(11.0f);
       
       barcode.renderBarcode(System.getProperty("user.dir")+"\\src\\tugas\\w11\\img\\"+kode+".png");
        
    }
    
