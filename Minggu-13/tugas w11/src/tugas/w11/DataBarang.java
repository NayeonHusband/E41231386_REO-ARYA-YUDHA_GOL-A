    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        kode.setEnabled(true);
        try{
            PreparedStatement pst = dbs.getConnection().prepareStatement("insert into data_barang(kd_barang,nama_barang,stok,harga,harga_jual,catatan) values (?,?,?,?,?,?)");
            pst.setString(1, kode.getText());
            pst.setString(2, nama.getText());
            pst.setInt(3, Integer.valueOf(stock.getText()));
            pst.setString(4,beli.getText());
            pst.setString(5, jual.getText());
            pst.setString(6, catatan.getText());
            
            pst.executeUpdate();
            generate(kode.getText());
            clear();
            updatetabel();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            
            
            
        }catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Data gagal Disimpan "+e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void Tabel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Tabel1MouseClicked
        int row = Tabel1.getSelectedRow();
        DefaultTableModel model =(DefaultTableModel) Tabel1.getModel();
        try{
            PreparedStatement pst = dbs.getConnection().prepareStatement("SELECT * FROM data_barang where kd_barang=?");
            pst.setString(1, model.getValueAt(row, 0).toString());
            ResultSet rs = pst.executeQuery();
            
            while(rs.next()){
                kode.setText(rs.getString(1));
                kode.setEnabled(false);
                nama.setText(rs.getString(2));
                stock.setText(rs.getString(3));
                beli.setText(rs.getString(4));
                jual.setText(rs.getString(5));
                catatan.setText(rs.getString(6));
                
//                barcode.setHorizontalAlignment(javax.swing.JLabel.CENTER);
                ImageIcon imgThisImg = new ImageIcon(System.getProperty("user.dir")+"\\src\\tugas\\w11\\img\\"+kode.getText()+".png");
//                rill.setLayout(new BorderLayout());
                
                
                JOptionPane.showMessageDialog(null, imgThisImg.getIconHeight()+" z "+ imgThisImg.getIconWidth());
              
                barcode1.setIcon(imgThisImg);
                repaint();
                revalidate();
            }
        }catch(Exception e){
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_Tabel1MouseClicked


